#include "3-calc.h"
/**
 * op_add - Returns a + b
 * @a: integer
 * @b: integer
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Return a - b
 * @a: integer
 * @b: integer
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Return a * b
 * @a: integer
 * @b: integer
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Return a / b
 * @a: integer
 * @b: integer
 * Return: Return a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Return a % b
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
